
// Asignar valores a las variables creadas en el Ej01
// ASIGNACION de variables: <tipo_de_dato> <nombre_variable> = expresion;
 
package javaintro;

public class JavaIntroEj02 {

    public static void main(String[] args) {
         
        String nombre = "MARTINA"; // SIEMPRE ENTRE " "
        
        // Tipo de dato primitivo: ENTEROS
        
        byte numerito = 5;
        short num = 100; 
        int numGrande = 2550; // El mas utilizado
        long numerote = 1000000;
        
         // Tipo de dato primitivo: DECIMALES
         
         float altura = 1.83f; // La coma se escribe como punto, y se le agrega la f al final para que lo almacene (lo tome).
         double km = 3556.67; // Mas grande que float
         
         // Tipo de dato primitivo: LOGICO
         
         boolean logico = true; // Se puede igualar a: (true)/(false), para darle valor
         
         // Tipo de dato primitivo: CARACTER
         
         char caracter = 'a'; // Se escribe char <nombre> = ' cualquier simbolo, letra, num, etc '. SIEMPRE va con COMILLAS SIMPLES (' ')
    }  
}
